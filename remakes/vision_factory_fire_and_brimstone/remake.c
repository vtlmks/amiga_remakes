// [=]===^=[ vector calculation ]====================================================^===[=]
static void precalc_rotated_vertices(void) {
	int16_t dst_coords_rotated[435];
	int16_t *output = dst_rotated_xy;
	int16_t rotation_top = 0;
	int16_t rotation_bottom = 0;
	int16_t distance_from_camera = 150;

	while(rotation_bottom != 256) {
		int16_t *src = org_coords_top;
		int16_t *dst = dst_coords_rotated;

		rotation_top &= 0xff;

		// Process top logo vertices
		while(1) {
			int16_t x = *src++;
			int16_t y = *src++;

			if(x == 0x4e75) break;

			int16_t angle = rotation_top;
			int16_t s = sin_table[angle];
			int16_t c = cos_table[angle];

			// Rotate around Y axis (x and z rotate, y stays)
			int32_t flat_z = 10;
			int32_t cam_z = flat_z * c - x * s;  // Z distance from camera after rotation
			int32_t rotated_x = flat_z * s + x * c;  // Rotated X position

			cam_z >>= 8;
			rotated_x >>= 8;

			int16_t depth = cam_z;  // For depth sorting
			cam_z += 150;  // Move away from camera

			// Perspective projection
			int16_t screen_x = (cam_z * rotated_x) / distance_from_camera;
			int16_t screen_y = (cam_z * y) / distance_from_camera;

			screen_x += (346 - 5) >> 1; //154;  // Center horizontally
			screen_y += 26 + 7;   // Vertical offset for top logo
			depth = (depth + 80) >> 3;  // Quantize to 29 layers

			*dst++ = depth;
			*dst++ = screen_x;
			*dst++ = screen_y;
		}

		// Process bottom logo vertices
		src = org_coords_bottom;
		rotation_bottom &= 0xff;

		while(1) {
			int16_t x = *src++;
			int16_t y = *src++;

			if(x == 0x4e75) break;

			int16_t angle = rotation_bottom;
			int16_t s = sin_table[angle];
			int16_t c = cos_table[angle];

			int32_t flat_z = 10;
			int32_t cam_z = flat_z * c - x * s;
			int32_t rotated_x = flat_z * s + x * c;

			cam_z >>= 8;
			rotated_x >>= 8;

			int16_t depth = cam_z;
			cam_z += 150;

			int16_t screen_x = (cam_z * rotated_x) / distance_from_camera;
			int16_t screen_y = (cam_z * y) / distance_from_camera;

			screen_x += (346 - 5) >> 1; //154;  // Center horizontally
			screen_y += 80 + 7;   // Vertical offset for bottom logo
			depth = (depth + 80) >> 3;

			*dst++ = depth;
			*dst++ = screen_x;
			*dst++ = screen_y;
		}

		*dst = 0x4e75;

		// Depth sort pass - reorganize by depth layers from -7 to 21
		for(int16_t layer = -7; layer <= 21; layer++) {
			dst = dst_coords_rotated;

			while(1) {
				int16_t depth = *dst++;
				if(depth == 0x4e75) break;

				if(depth == layer) {
					*output++ = *dst++;  // screen_x
					*output++ = *dst++;  // screen_y
				} else {
					dst += 2;
				}
			}
		}

		*output++ = 0x4e75;

		rotation_top -= 2;
		rotation_bottom += 2;
	}
}


